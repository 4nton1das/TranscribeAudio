# prompts.py

def get_system_prompt(task, target_language=None):
    """Возвращает системный промпт для выбранной задачи"""
    if task == "correct":
        return (
            "Ты профессиональный редактор. Исправь ошибки в транскрипции, "
            "добавь пунктуацию, исправь опечатки и приведи текст в читаемый вид. "
            "Сохрани оригинальный смысл. Не добавляй пояснений."
        )

    elif task == "summarize":
        return (
            "Ты опытный редактор. Сократи текст, оставив только основные идеи. "
            "Длина суммаризации должна быть примерно 20% от исходного текста. "
            "Сохрани ключевые факты и выводы. Выведи только сокращенный текст."
        )

    elif task == "translate":
        return (
            f"Ты профессиональный переводчик. Переведи текст на язык {target_language}, "
            "сохраняя стиль и смысл оригинала. Не добавляй пояснений."
        )

    # Для обычной транскрипции не нужен промпт
    return None
